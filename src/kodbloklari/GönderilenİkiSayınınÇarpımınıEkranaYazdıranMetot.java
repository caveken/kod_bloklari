package kodbloklari;

public class GönderilenİkiSayınınÇarpımınıEkranaYazdıranMetot {
    static void carp(int sayi1, int sayi2) {
        System.out.println("Sayıların carpımı = " + (sayi1 * sayi2));
    }
    public static void main(String[] args) {

        carp(15,5);
    }
    /*
    Herhangi iki sayıyı metodun parametresi olarak giriyoruz.

Metot da bu sayıları çarpıp sonucu ekrana yazdırıyor.
     */
}
